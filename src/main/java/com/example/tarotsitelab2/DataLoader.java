    private void loadSpreadTemplates() {
        SpreadTemplate day = new SpreadTemplate();
        day.setName("Карта дня");
        day.setDescription("Проста відповідь або порада на сьогодні");
        templateRepository.save(day);
        savePosition(day, 0, "Порада", "Загальна енергія дня");

        SpreadTemplate timeline = new SpreadTemplate();
        timeline.setName("Три карти");
        timeline.setDescription("Минуле, теперішнє, майбутнє");
        templateRepository.save(timeline);
        savePosition(timeline, 0, "Минуле", "Корені проблеми");
        savePosition(timeline, 1, "Теперішнє", "Поточний стан");
        savePosition(timeline, 2, "Майбутнє", "Ймовірний результат");
    }

    private Card createCard(String name, String arcana, String suit, String element, String imgFile, String up, String rev) {
        Card c = new Card();
        c.setName(name);
        c.setArcana(arcana);
        c.setSuit(suit);
        c.setElement(element);
        c.setMeaningUp(up);
        c.setMeaningRev(rev);
        c.setImageUrl("/images/cards/" + imgFile);
        return c;
    }

    private void savePosition(SpreadTemplate t, int idx, String label, String desc) {
        SpreadPosition p = new SpreadPosition();
        p.setTemplate(t);
        p.setPosIndex(idx);
        p.setLabel(label);
        p.setContextDescription(desc);
        positionRepository.save(p);
    }
